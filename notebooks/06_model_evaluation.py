import json
import os

# Load predictions from the Full Fine-Tuned model
# This file contains the model outputs along with the original inputs and references
with open("../outputs/full_ft/predictions_full_ft.json", encoding="utf-8") as f:
    full_data = json.load(f)

# Load predictions from the LoRA fine-tuned model
# We will compare these outputs with the Full FT outputs for qualitative analysis
with open("../outputs/lora/predictions_lora.json", encoding="utf-8") as f:
    lora_data = json.load(f)

# Create a directory to store evaluation results if it does not already exist
os.makedirs("../evaluation_results", exist_ok=True)

# Open a text file where we will store a few qualitative comparison examples
with open("../evaluation_results/qualitative_examples.txt", "w", encoding="utf-8") as f:
    for i in range(min(5, len(full_data["predictions"]))):
        idiom = full_data["predictions"][i]["input"].replace("Convert the following English idiom into its natural Urdu equivalent: ", "")
        # Ground truth Urdu translation from the dataset
        ref   = full_data["predictions"][i]["reference"]
        # Prediction generated by the Full Fine-Tuned model
        full_pred = full_data["predictions"][i]["prediction"]
        # Prediction generated by the LoRA fine-tuned model
        lora_pred = lora_data["predictions"][i]["prediction"]

        f.write(f"Example {i+1}\n")
        f.write(f"Input     : {idiom}\n")
        f.write(f"Reference : {ref}\n")
        f.write(f"Full FT   : {full_pred}\n")
        f.write(f"LoRA      : {lora_pred}\n")
        f.write("-" * 50 + "\n\n")

print("Saved to evaluation_results/qualitative_examples.txt")