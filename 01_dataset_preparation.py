import pandas as pd
import os
from sklearn.model_selection import train_test_split
from datasets import Dataset, DatasetDict

# Create the folders we will use to store processed files
os.makedirs('data', exist_ok=True)
os.makedirs('data_pipeline', exist_ok=True)
os.makedirs('hf_dataset', exist_ok=True)

# Read the Excel file containing Urdu idioms and their English translations
excel_file_name = 'Urdu_Idioms_with_English_Translation.xlsx'
df = pd.read_excel(excel_file_name, header=None, names=['Urdu Idiom', 'English Translation'])

# Remove any incomplete rows where either the Urdu idiom or the English translation is missing
df = df.dropna(subset=['Urdu Idiom', 'English Translation'])

df.to_csv('data/raw_converted.csv', index=False)

# This function assigns a rough thematic category to each idiom
# based on keywords found in the English translation
# It is a simple rule-based categorisation approach for analysis purposes
def categorize_idiom(text):
    text = str(text).lower()
    if any(word in text for word in ['friend', 'people', 'talk', 'meet', 'party', 'social', 'praise', 'oneself', 'company', 'guest', 'man', 'woman', 'family']): return 'social'
    if any(word in text for word in ['hide', 'secret', 'quiet', 'tell', 'cover', 'spill', 'beans', 'disclose', 'mask', 'spy']): return 'secrecy'
    if any(word in text for word in ['health', 'sick', 'doctor', 'pain', 'feel', 'weather', 'fit', 'ill', 'recover', 'medicine', 'die', 'alive']): return 'health'
    if any(word in text for word in ['win', 'succeed', 'top', 'money', 'goal', 'purpose', 'high', 'achieve', 'prosper', 'price', 'perfect', 'fail', 'lose']): return 'success'
    if any(word in text for word in ['cry', 'angry', 'sad', 'happy', 'love', 'hate', 'heart', 'temper', 'furious', 'joy', 'grief', 'courage', 'fear']): return 'emotions'
    if any(word in text for word in ['work', 'job', 'boss', 'office', 'hard', 'labor', 'grind', 'sweat', 'task', 'duty', 'practice', 'effort']): return 'work'
    if any(word in text for word in ['dog', 'cat', 'bird', 'fish', 'animal', 'horse', 'cow', 'lion', 'snake']): return 'animals'
    if any(word in text for word in ['arm', 'leg', 'head', 'eye', 'back', 'hand', 'face', 'tooth', 'bone', 'blood', 'foot']): return 'body'
    if any(word in text for word in ['tree', 'rain', 'storm', 'moon', 'sun', 'water', 'wind', 'sky', 'fire', 'earth', 'sea']): return 'nature'
    if any(word in text for word in ['penny', 'dime', 'time', 'clock', 'hour', 'day', 'gold', 'year', 'month', 'cost']): return 'time_money'
    if any(word in text for word in ['bite', 'cut', 'run', 'walk', 'jump', 'push', 'pull', 'throw', 'catch', 'fall', 'break']): return 'action'
    if any(word in text for word in ['wise', 'fool', 'mind', 'learn', 'ignorant', 'truth', 'lie', 'think', 'thought', 'brain', 'smart', 'clever', 'stupid', 'idiot', 'know', 'sense', 'better', 'worse', 'reason']): return 'wisdom_folly'
    if any(word in text for word in ['god', 'devil', 'fortune', 'heaven', 'hell', 'pray', 'bless', 'sin', 'fate', 'luck', 'chance', 'destiny', 'angel', 'demon', 'lord']): return 'religion_fate'
    if any(word in text for word in ['word', 'speak', 'talk', 'tongue', 'silent', 'voice', 'say', 'tell', 'listen', 'hear', 'shout', 'whisper', 'argue']): return 'communication'
    if any(word in text for word in ['quarrel', 'fight', 'sword', 'kill', 'enemy', 'danger', 'weapon', 'war', 'battle', 'strike', 'beat', 'harm', 'threat', 'defend', 'attack']): return 'conflict'
    if any(word in text for word in ['little', 'great', 'small', 'big', 'huge', 'tiny', 'half', 'double', 'much', 'many', 'nothing', 'everything', 'empty', 'full', 'heavy', 'light']): return 'quantity'
    return 'other'

df['category'] = df['English Translation'].apply(categorize_idiom)

df.to_csv('dataset_final.csv', index=False, encoding='utf-8-sig')

# Split the dataset into training, validation, and test sets    
train_df, temp_df = train_test_split(df, test_size=0.20, random_state=42)
# Second split: divide the temporary set equally into validation and test
val_df, test_df = train_test_split(temp_df, test_size=0.50, random_state=42)
train_df.to_csv('data/train.csv', index=False, encoding='utf-8-sig')
val_df.to_csv('data/val.csv', index=False, encoding='utf-8-sig')
test_df.to_csv('data/test.csv', index=False, encoding='utf-8-sig')

hf_dataset = DatasetDict({
    'train': Dataset.from_pandas(train_df),
    'validation': Dataset.from_pandas(val_df),
    'test': Dataset.from_pandas(test_df)
})

for split in hf_dataset.keys():
    if "__index_level_0__" in hf_dataset[split].column_names:
        hf_dataset[split] = hf_dataset[split].remove_columns("__index_level_0__")

hf_dataset.save_to_disk('hf_dataset')

